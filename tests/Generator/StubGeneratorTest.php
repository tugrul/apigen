    private function gen(string $naming = 'default'): StubGenerator
    {
        $strategy = match ($naming) {
            'suffix' => new SuffixNamingStrategy('Stub'),
            'subns'  => new SubNamespaceNamingStrategy('Generated'),
            default  => new DefaultNamingStrategy(),
        };

        return new StubGenerator($this->outputDir, $strategy);
    }

