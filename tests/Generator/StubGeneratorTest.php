    // ── Conflict detection ────────────────────────────────────────────────────
    //
    // We use FALLBACK_FULL_NAMESPACE so the output path is fully deterministic
    // in the test environment regardless of whether a composer.json is found.

    private function genForConflict(): StubGenerator
    {
        $resolver = new \Tugrul\ApiGen\Generator\OutputPathResolver(
            $this->outputDir,
            \Tugrul\ApiGen\Generator\OutputPathResolver::FALLBACK_FULL_NAMESPACE,
        );

        return new StubGenerator($this->outputDir, new DefaultNamingStrategy(), $resolver);
    }

    public function test_impl_suffix_used_when_file_exists_and_is_not_generated(): void
    {
        // Ask the resolver itself where it will write the file — then pre-create it
        $rc           = new \ReflectionClass(SimpleApi::class);
        $resolver     = new \Tugrul\ApiGen\Generator\OutputPathResolver(
            $this->outputDir,
            \Tugrul\ApiGen\Generator\OutputPathResolver::FALLBACK_FULL_NAMESPACE,
        );
        $expectedPath = $resolver->resolve($rc, 'Tugrul\\ApiGen\\Tests\\Fixtures', 'SimpleApi');

        @mkdir(dirname($expectedPath), 0755, true);
        file_put_contents($expectedPath, '<?php // hand-written, not generated');

        $stub = $this->genForConflict()->generate(SimpleApi::class);

        self::assertSame('SimpleApiImpl', $stub->stubShortName);
    }

    public function test_no_impl_suffix_when_existing_file_is_auto_generated(): void
    {
        $rc           = new \ReflectionClass(SimpleApi::class);
        $resolver     = new \Tugrul\ApiGen\Generator\OutputPathResolver(
            $this->outputDir,
            \Tugrul\ApiGen\Generator\OutputPathResolver::FALLBACK_FULL_NAMESPACE,
        );
        $expectedPath = $resolver->resolve($rc, 'Tugrul\\ApiGen\\Tests\\Fixtures', 'SimpleApi');

        @mkdir(dirname($expectedPath), 0755, true);
        file_put_contents($expectedPath, '<?php // AUTO-GENERATED by Tugrul\ApiGen\Generator\StubGenerator.');

        $stub = $this->genForConflict()->generate(SimpleApi::class);

        self::assertSame('SimpleApi', $stub->stubShortName); // no Impl — safe to overwrite
    }

    // ── Helpers ───────────────────────────────────────────────────────────────

    private function rmdirRecursive(string $dir): void
    {
        if (!is_dir($dir)) {
            return;
        }

        foreach (glob($dir . '/*') ?: [] as $entry) {
            is_dir($entry) ? $this->rmdirRecursive($entry) : unlink($entry);
        }

        rmdir($dir);
    }
