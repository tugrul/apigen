    /**
     * Extract a method body from generated code by finding the opening brace
     * of the method and then tracking brace depth to find the matching closing brace.
     * This correctly handles path params like /users/{id} which contain '}' inside strings.
     */
    private function extractMethodBody(string $code, string $methodName): string
    {
        $start = strpos($code, 'function ' . $methodName);
        if ($start === false) {
            return '';
        }

        // Find the opening brace of the method body
        $open = strpos($code, '{', $start);
        if ($open === false) {
            return '';
        }

        // Walk forward tracking brace depth to find the true closing brace
        $depth = 0;
        $len   = strlen($code);

        for ($i = $open; $i < $len; $i++) {
            if ($code[$i] === '{') {
                $depth++;
            } elseif ($code[$i] === '}') {
                $depth--;
                if ($depth === 0) {
                    return substr($code, $open, $i - $open + 1);
                }
            }
        }

        return substr($code, $open);
    }

